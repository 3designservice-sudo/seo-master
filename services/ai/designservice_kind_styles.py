def get_kind_instructions(kind: str) -> dict:
    """Get per-kind prompt instructions.

    Returns dict with keys: structure_hint, tone, quirks, temperature_adj.
    Falls back to info_basic for unknown kinds.
    """
    return KIND_STYLES.get(kind, KIND_STYLES["info_basic"])


def get_random_author_tail() -> str:
    """Random author-tail snippet for description extension and author block."""
    return random.choice(_AUTHOR_TAILS)
